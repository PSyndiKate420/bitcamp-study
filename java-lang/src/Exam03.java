public class Exam03 {
  public static void main(String[] args) {
    System.out.println("Hello!");
  }
  void m2() {
    System.out.println("Hello!");
  }
  // void m2()를 실행시키고 싶다면, main method에서 void m2()를 호출해야 함.
}

// class가 실행되려면 main method가 존재해야 한다.
// ex) public static void main(String[] args)